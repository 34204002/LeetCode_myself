package 链表;

/**
 * LeetCode 206. 反转链表
 * 
 * 【题目描述】
 * 给你单链表的头节点 head ，请你反转链表，并返回反转后的链表。
 * 
 * 【示例】
 * 输入：head = [1,2,3,4,5]
 * 输出：[5,4,3,2,1]
 * 
 * 【解题思路】
 * 核心思想：迭代法，逐个反转指针方向
 * 
 * 你的实现采用了三指针方式：
 * - p1：已反转部分的头节点
 * - p2：当前处理的节点（探路者）
 * - p：临时保存 p2 的前一个节点
 * 
 * 算法流程：
 * 1. 初始化 p1=null, p2=head
 * 2. 当 p2.next != null 时循环：
 *    - p = p2（保存当前节点）
 *    - p2 = p2.next（p2 前进一步）
 *    - p.next = p1（反转 p 的指针）
 *    - p1 = p（p1 前进一步）
 * 3. 最后处理 p2（原链表最后一个节点）
 * 
 * 【执行示例】1 -> 2 -> 3 -> 4 -> 5
 * 
 * 初始：p1=null, p2=1
 * 
 * 第1轮：p=1, p2=2, 1.next=null, p1=1
 * 状态：1 -> null, p2=2 -> 3 -> 4 -> 5
 * 
 * 第2轮：p=2, p2=3, 2.next=1, p1=2
 * 状态：2 -> 1 -> null, p2=3 -> 4 -> 5
 * 
 * 第3轮：p=3, p2=4, 3.next=2, p1=3
 * 状态：3 -> 2 -> 1 -> null, p2=4 -> 5
 * 
 * 第4轮：p=4, p2=5, 4.next=3, p1=4
 * 状态：4 -> 3 -> 2 -> 1 -> null, p2=5
 * 
 * 循环结束：p2.next==null
 * 最后：5.next=4
 * 结果：5 -> 4 -> 3 -> 2 -> 1 -> null ✓
 * 
 * 【时间复杂度】O(n) - 遍历一次链表
 * 【空间复杂度】O(1) - 只使用三个指针
 * 
 * 【关键点】
 * - 第5-6行：空链表或单节点直接返回
 * - 第8-9行：初始化 p1=null, p2=head
 * - 第10-15行：循环反转，p2 作为"探路者"提前一步
 * - 第16行：处理最后一个节点的反转
 * - 第17行：p2 现在是新链表的头节点
 */
public class LC_206_reverseList_1 {
    public ListNode reverseList(ListNode head) {
        if(head==null||head.next==null)
            return head;
        ListNode p;
        ListNode p1=null;   // 已反转部分的头
        ListNode p2=head;   // 当前处理的节点
        while (p2.next!=null){
            p=p2;           // 保存当前节点
            p2=p2.next;     // p2 前进一步
            p.next=p1;      // 反转当前节点的指针
            p1=p;           // p1 前进一步
        }
        p2.next=p1;         // 处理最后一个节点
        return p2;          // p2 是新链表的头
    }
}
