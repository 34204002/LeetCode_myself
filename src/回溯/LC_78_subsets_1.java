package 回溯;

import java.util.ArrayList;
import java.util.List;

/**
 * LeetCode 78. 子集 - 解法一：回溯算法
 * 
 * 【题目描述】
 * 给你一个整数数组 nums ，数组中的元素互不相同。返回该数组所有可能的子集（幂集）。
 * 解集不能包含重复的子集。你可以按任意顺序返回解集。
 * 
 * 【示例】
 * 输入：nums = [1,2,3]
 * 输出：[[],[1],[2],[1,2],[3],[1,3],[2,3],[1,2,3]]
 * 
 * 【解题思路】
 * 核心思想：回溯算法（深度优先搜索 + 状态重置）
 * 
 * 算法流程：
 * 1. 从索引0开始，每次选择一个元素加入路径
 * 2. 每次进入DFS时，都将当前路径加入结果集（每个节点都是一个有效子集）
 * 3. 从当前索引往后选择，避免重复组合
 * 4. 回溯：撤销选择，尝试其他元素
 * 
 * 【执行示例】nums = [1,2,3]
 * 
 * 初始：path=[], 加入结果 → [[]]
 * 
 * 第1层：i=0，选择1
 *   path=[1], 加入结果 → [[],[1]]
 *   第2层：i=1，选择2
 *     path=[1,2], 加入结果 → [[],[1],[1,2]]
 *     第3层：i=2，选择3
 *       path=[1,2,3], 加入结果 → [[],[1],[1,2],[1,2,3]]
 *       回溯：移除3 → path=[1,2]
 *     回溯：移除2 → path=[1]
 *   第2层：i=2，选择3
 *     path=[1,3], 加入结果 → [[],[1],[1,2],[1,2,3],[1,3]]
 *     回溯：移除3 → path=[1]
 *   回溯：移除1 → path=[]
 * 
 * 第1层：i=1，选择2
 *   path=[2], 加入结果 → [[],[1],[1,2],[1,2,3],[1,3],[2]]
 *   第2层：i=2，选择3
 *     path=[2,3], 加入结果 → [[],[1],[1,2],[1,2,3],[1,3],[2],[2,3]]
 *     回溯：移除3 → path=[2]
 *   回溯：移除2 → path=[]
 * 
 * 第1层：i=2，选择3
 *   path=[3], 加入结果 → [[],[1],[1,2],[1,2,3],[1,3],[2],[2,3],[3]]
 *   回溯：移除3 → path=[]
 * 
 * 结果：8个子集（2^3）✓
 * 
 * 【时间复杂度】O(n×2^n) - 共有2^n个子集，每个子集平均长度O(n)
 * 【空间复杂度】O(n) - 递归栈深度O(n)，不包括结果存储空间
 * 
 * 【关键点】
 * - 第8行：创建结果列表存储所有子集
 * - 第9行：启动DFS，从索引0开始
 * - 第13行：每次进入DFS都将当前路径加入结果（每个节点都是有效子集）
 * - 第14行：从index开始遍历，避免向前选择造成重复
 * - 第15行：做选择 - 将元素加入路径
 * - 第16行：递归进入下一层，索引+1
 * - 第17行：撤销选择 - 回溯，移除最后一个元素
 * 
 * 【注意】
 * - 第13行：必须在循环之前添加路径，确保空集和所有中间状态都被记录
 * - 第14行：i从index开始而非0，保证每个元素只被选择一次，避免重复组合
 * - 第17行：使用removeLast()移除最后一个元素，实现回溯
 */
public class LC_78_subsets_1 {
    public List<List<Integer>> subsets(int[] nums) {
        List<List<Integer>> list=new ArrayList<>();
        dfs(nums,0,new ArrayList<>(),list);
        return list;
    }
    private static void dfs(int[] nums,int index,List<Integer> path,List<List<Integer>> list){
        list.add(new ArrayList<>(path));
        for (int i = index; i < nums.length; i++) {
            path.add(nums[i]);
            dfs(nums,i+1,path,list);
            path.removeLast();
        }
    }

}
