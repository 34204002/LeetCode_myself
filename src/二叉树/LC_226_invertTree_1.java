package 二叉树;

/**
 * LeetCode 226. 翻转二叉树 - 解法一：递归（DFS）
 * 
 * 【题目描述】
 * 给你一棵二叉树的根节点 root ，翻转这棵二叉树，并返回其根节点。
 * 翻转操作：交换每个节点的左右子树。
 * 
 * 【示例】
 * 输入：root = [4,2,7,1,3,6,9]
 * 输出：[4,7,2,9,6,3,1]
 * 
 * 【解题思路】
 * 核心思想：前序遍历，先交换当前节点的左右子树，再递归处理子节点
 * 
 * 算法流程：
 * 1. 如果 root == null，返回 null（递归终止条件）
 * 2. 交换当前节点的左右子树：
 *    - 用临时变量保存左子树
 *    - 左子树 = 右子树
 *    - 右子树 = 临时变量
 * 3. 递归翻转左子树
 * 4. 递归翻转右子树
 * 5. 返回根节点
 * 
 * 【执行示例】
 *       4              4
 *      / \           /   \
 *     2   7   ->    7     2
 *    / \ / \       / \   / \
 *   1  3 6  9     9  6  3   1
 * 
 * 第1步：交换节点4的左右子树 (2↔7)
 * 第2步：递归处理节点7，交换其子树 (6↔9)
 * 第3步：递归处理节点2，交换其子树 (1↔3)
 * 结果：✓
 * 
 * 【时间复杂度】O(n) - 每个节点访问一次
 * 【空间复杂度】O(h) - h为树的高度，递归栈的开销
 * 
 * 【关键点】
 * - 第5-6行：递归终止条件（空节点返回null）
 * - 第7-9行：交换当前节点的左右子树
 * - 第10-11行：递归处理左右子树
 * - 第13行：返回翻转后的根节点
 */
public class LC_226_invertTree_1 {
    public TreeNode invertTree(TreeNode root) {
        if(root==null)
            return null;
        TreeNode node=root.left;
        root.left=root.right;
        root.right=node;
        invertTree(root.left);
        invertTree(root.right);

        return root;

    }
}
