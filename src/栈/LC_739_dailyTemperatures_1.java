package 栈;

import java.util.Stack;

/**
 * LeetCode 739. 每日温度 - 解法一：单调递减栈
 *
 * 【题目描述】
 * 给定一个整数数组 temperatures，表示每天的温度，返回一个数组 answer，
 * 其中 answer[i] 是指对于第 i 天，下一个更高温度出现在几天后。
 * 如果气温在这之后都不会升高，请在该位置用 0 来代替。
 *
 * 【示例】
 * 输入：temperatures = [73,74,75,71,69,72,76,73]
 * 输出：[1,1,4,2,1,1,0,0]
 * 输入：temperatures = [30,40,50,60]
 * 输出：[1,1,1,0]
 *
 * 【解题思路】
 * 核心思想：单调递减栈——栈中存放尚未找到"下一个更高温度"的日期
 *
 * 维护一个从栈底到栈顶温度递减的栈（栈顶温度最小）：
 *   - 遍历每天的温度
 *   - 当栈非空且当前温度 > 栈顶温度时：栈顶元素"等到了"更高的温度，
 *     弹出栈顶并计算天数差（当前下标 - 栈顶下标），填入结果数组
 *   - 将当前天的 (下标, 温度) 压入栈中
 *
 * 算法流程：
 * 1. 初始化结果数组 result，默认值为 0（未找到更高温度）
 * 2. 使用 Stack<int[]> 存储 (下标, 温度)
 * 3. 遍历 temperatures：
 *    a. while 栈非空 且 栈顶温度 < 当前温度：弹出栈顶，result[栈顶下标] = 当前下标 - 栈顶下标
 *    b. 将 (当前下标, 当前温度) 压入栈
 * 4. 遍历结束后 result 即答案（未弹出的元素 result 保持 0）
 *
 * 【执行示例】temperatures = [73,74,75,71,69,72,76,73]
 *
 * i=0 t=73: 栈空 → push [0,73]          栈=[(0,73)]
 * i=1 t=74: 73<74 → pop (0,73), res[0]=1, push [1,74]  栈=[(1,74)]
 * i=2 t=75: 74<75 → pop (1,74), res[1]=1, push [2,75]  栈=[(2,75)]
 * i=3 t=71: 75>71 → push [3,71]         栈=[(2,75),(3,71)]
 * i=4 t=69: 71>69 → push [4,69]         栈=[(2,75),(3,71),(4,69)]
 * i=5 t=72: 69<72 → pop (4,69), res[4]=1, 71<72 → pop (3,71), res[3]=2,
 *           75>72 → push [5,72]          栈=[(2,75),(5,72)]
 * i=6 t=76: 72<76 → pop (5,72), res[5]=1, 75<76 → pop (2,75), res[2]=4,
 *           push [6,76]                  栈=[(6,76)]
 * i=7 t=73: 76>73 → push [7,73]         栈=[(6,76),(7,73)]
 *
 * result = [1,1,4,2,1,1,0,0] ✓
 *
 * 【时间复杂度】O(n) - 每个元素最多入栈一次、出栈一次
 * 【空间复杂度】O(n) - 栈在最坏情况下（严格递减序列）存储所有元素
 *
 * 【关键点】
 * - 第8行：栈存储 int[]{下标, 温度}，方便同时获取位置和值
 * - 第10-16行：while 循环不断弹出比当前温度小的栈顶，填入结果
 * - 第14行：break 保证栈的单调递减性质（遇到更大的栈顶停止弹出）
 * - 第17行：每个元素都会入栈，等待后续更高的温度来"唤醒"它
 *
 * 【注意】
 * - 第7行：result 默认值为 0，与"之后没有更高温度"的返回值一致
 * - 第11行：比较用 < 而非 <=，题目要求"更高"温度，等于不算
 * - 第12行：弹出前先保存下标 index，因为 pop() 后无法再获取
 * - 第14行：else break 很关键，保证栈单调递减；遗漏会导致错误弹出
 */
public class LC_739_dailyTemperatures_1 {
    public int[] dailyTemperatures(int[] temperatures) {
        int[] result=new int[temperatures.length];
        Stack<int[]> stack=new Stack<>();
        for (int i = 0; i < temperatures.length; i++) {
            while (!stack.isEmpty()){
                if (stack.peek()[1]<temperatures[i]) {
                    int index = stack.pop()[0];
                    result[index]=i-index;
                }else
                    break;
            }
            stack.add(new int[]{i,temperatures[i]});
        }
        return result;
    }
}
