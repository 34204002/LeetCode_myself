package 栈;

import java.util.Stack;

/**
 * LeetCode 20. 有效的括号
 * 
 * 【题目描述】
 * 给定一个只包括 '('，')'，'{'，'}'，'['，']' 的字符串 s ，判断字符串是否有效。
 * 有效字符串需满足：
 * 1. 左括号必须用相同类型的右括号闭合。
 * 2. 左括号必须以正确的顺序闭合。
 * 3. 每个右括号都有一个对应的相同类型的左括号。
 * 
 * 【示例】
 * 输入：s = "()"
 * 输出：true
 * 
 * 输入：s = "()[]{}"
 * 输出：true
 * 
 * 输入：s = "(]"
 * 输出：false
 * 
 * 【解题思路】
 * 核心思想：使用栈来匹配括号
 * 
 * 算法流程：
 * 1. 遍历字符串中的每个字符
 * 2. 如果是左括号（(、[、{），压入栈中
 * 3. 如果是右括号（)、]、}）：
 *    - 检查栈是否为空，为空则返回 false
 *    - 检查栈顶元素是否为对应的左括号
 *    - 如果匹配则弹出栈顶元素，否则返回 false
 * 4. 遍历结束后，检查栈是否为空
 *    - 栈为空说明所有括号都匹配，返回 true
 *    - 栈不为说明有未匹配的左括号，返回 false
 * 
 * 【执行示例】s = "()[]{}"
 * 
 * 第1轮：c='('，是左括号，压栈 → stack: ['(']
 * 第2轮：c=')'，是右括号，栈顶是 '('，匹配 → 弹栈 → stack: []
 * 第3轮：c='['，是左括号，压栈 → stack: ['[']
 * 第4轮：c=']'，是右括号，栈顶是 '['，匹配 → 弹栈 → stack: []
 * 第5轮：c='{'，是左括号，压栈 → stack: ['{']
 * 第6轮：c='}'，是右括号，栈顶是 '{'，匹配 → 弹栈 → stack: []
 * 
 * 遍历结束，栈为空 → 返回 true ✓
 * 
 * 【时间复杂度】O(n) - 遍历一次字符串
 * 【空间复杂度】O(n) - 栈最多存储 n/2 个左括号
 * 
 * 【关键点】
 * - 第7行：创建栈用于存储左括号
 * - 第8-33行：遍历字符串，逐个处理字符
 * - 第10-11行：遇到左括号则压栈
 * - 第13-32行：遇到右括号则进行匹配检查
 * - 第14-18行：处理 ')' 的情况
 * - 第19-23行：处理 ']' 的情况
 * - 第24-28行：处理 '}' 的情况
 * - 第30-31行：栈为空时遇到右括号，直接返回 false
 * - 第35-36行：遍历结束后检查栈是否为空
 */
public class LC_20_isValid_1 {
    public boolean isValid(String s) {
        Stack<Character> stack=new Stack<>();
        for (int i = 0; i < s.length(); i++) {
            char c=s.charAt(i);
            if(c=='('||c=='['||c=='{'){
                stack.add(c);           // 左括号压栈
            }else{
                if(!stack.isEmpty()){
                    if (c == ')') {     // 处理右圆括号
                        if (stack.peek() != '(')
                            return false;
                        else
                            stack.pop();
                    } else if (c == ']') {  // 处理右方括号
                        if (stack.peek() != '[')
                            return false;
                        else
                            stack.pop();
                    } else if (c == '}') {  // 处理右花括号
                        if (stack.peek() != '{')
                            return false;
                        else
                            stack.pop();
                    }
                }else {
                    return false;       // 栈为空但遇到右括号
                }
            }
        }
        if(!stack.isEmpty())
            return false;               // 有未匹配的左括号
        return true;                    // 所有括号都匹配
    }
}
