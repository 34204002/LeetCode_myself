package 矩阵;

/**
 * LeetCode 73. 矩阵置零
 * 
 * 【题目描述】
 * 给定一个 m x n 的矩阵，如果一个元素为 0 ，则将其所在行和列的所有元素都设为 0 。
 * 请使用原地算法。
 * 
 * 【示例】
 * 输入：matrix = [[1,1,1],[1,0,1],[1,1,1]]
 * 输出：[[1,0,1],[0,0,0],[1,0,1]]
 * 
 * 输入：matrix = [[0,1,2,0],[3,4,5,2],[1,3,1,5]]
 * 输出：[[0,0,0,0],[0,4,5,0],[0,3,1,0]]
 * 
 * 【解题思路】
 * 核心思想：使用两个标记数组记录需要置零的行和列
 * 
 * 算法流程：
 * 1. 创建两个标记数组：
 *    - y[i]：标记第 i 行是否需要置零
 *    - x[j]：标记第 j 列是否需要置零
 * 
 * 2. 第一次遍历矩阵：
 *    - 如果 matrix[i][j] == 0，标记 y[i]=1 和 x[j]=1
 * 
 * 3. 第二次遍历标记数组：
 *    - 根据 y 数组，将对应行全部置零
 *    - 根据 x 数组，将对应列全部置零
 * 
 * 【执行示例】matrix = [[1,1,1],[1,0,1],[1,1,1]]
 * 
 * 第一次遍历：
 * i=0: 无 0
 * i=1: matrix[1][1]=0 → y[1]=1, x[1]=1
 * i=2: 无 0
 * 
 * y = [0, 1, 0]
 * x = [0, 1, 0]
 * 
 * 第二次遍历（处理行）：
 * y[1]=1 → 第1行置零 → [[1,1,1],[0,0,0],[1,1,1]]
 * 
 * 第三次遍历（处理列）：
 * x[1]=1 → 第1列置零 → [[1,0,1],[0,0,0],[1,0,1]]
 * 
 * 结果：[[1,0,1],[0,0,0],[1,0,1]] ✓
 * 
 * 【时间复杂度】O(m×n)
 * - 第一次遍历矩阵 O(m×n)
 * - 第二次遍历置零行 O(m×n)
 * - 第三次遍历置零列 O(m×n)
 * - 总复杂度 O(m×n)
 * 
 * 【空间复杂度】O(m+n) - 两个标记数组
 * 
 * 【优化建议】
 * - 可以使用矩阵的第一行和第一列作为标记数组，实现 O(1) 空间
 * - 需要额外变量记录第一行和第一列本身是否需要置零
 * 
 * 【关键点】
 * - 第5-6行：创建行标记数组 y 和列标记数组 x
 * - 第7-14行：第一次遍历，标记含 0 的行和列
 * - 第15-21行：根据行标记，将对应行全部置零
 * - 第22-28行：根据列标记，将对应列全部置零
 * 
 * 【为什么要分两次遍历？】
 * - 如果在第一次遍历时直接置零，会影响后续判断
 * - 例如 matrix[0][0]=0 会导致整行整列置零，误判其他位置
 * - 先标记再统一处理，保证正确性
 * 
 * 【注意】
 * - 你的变量命名 x 和 y 与实际含义相反：
 *   · y 数组标记行（应该是 row 或 rows）
 *   · x 数组标记列（应该是 col 或 cols）
 * - 建议改为更清晰的命名
 */
public class LC_73_setZeroes_1 {
    public static void setZeroes(int[][] matrix) {
        int[] y=new int[matrix.length];      // 行标记数组
        int[] x=new int[matrix[0].length];   // 列标记数组
        // 第一次遍历：标记含 0 的行和列
        for (int i = 0; i < matrix.length; i++) {
            for (int j = 0; j < matrix[0].length; j++) {
                if(matrix[i][j]==0){
                    y[i]=1;  // 标记第 i 行
                    x[j]=1;  // 标记第 j 列
                }
            }
        }
        // 第二次遍历：将标记的行置零
        for (int i = 0; i < y.length; i++) {
            if(y[i]==1){
                for (int j = 0; j < x.length; j++) {
                    matrix[i][j]=0;
                }
            }
        }
        // 第三次遍历：将标记的列置零
        for (int i = 0; i < x.length; i++) {
            if(x[i]==1){
                for (int i1 = 0; i1 < y.length; i1++) {
                    matrix[i1][i]=0;
                }
            }
        }
    }
}
