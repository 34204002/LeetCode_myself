package 贪心;

/**
 * LeetCode 121. 买卖股票的最佳时机 - 解法一：贪心（一次遍历）
 *
 * 【题目描述】
 * 给定一个数组 prices，它的第 i 个元素 prices[i] 表示一支给定股票第 i 天的价格。
 * 你只能选择某一天买入这只股票，并选择在未来的某一个不同的日子卖出该股票。
 * 设计一个算法来计算你所能获取的最大利润。返回你可以从这笔交易中获取的最大利润。
 * 如果你不能获取任何利润，返回 0。
 *
 * 【示例】
 * 输入：[7,1,5,3,6,4]
 * 输出：5
 * 解释：在第 2 天（价格 = 1）买入，在第 5 天（价格 = 6）卖出，利润 = 6-1 = 5
 * 输入：prices = [7,6,4,3,1]
 * 输出：0
 * 解释：价格持续下跌，任何买入都会亏损，不交易是最优选择
 *
 * 【解题思路】
 * 核心思想：一次遍历，同时维护「历史最低买入价」和「当前最大利润」
 *
 * 遍历价格数组时，每个价格处有两种可能的操作：
 *   - 作为卖出价：计算当前价格与历史最低价的差值，更新最大利润
 *   - 作为买入价：如果当前价格比历史最低价更低，更新最低买入价
 *
 * 算法流程：
 * 1. 初始化 maxP=0（最大利润），minPrice=prices[0]（历史最低买入价）
 * 2. 遍历数组（可从第0天开始，因为 i=0 时买入卖出同一天的利润为0）
 * 3. 若 prices[i] < minPrice，说明找到更低买入点，更新 minPrice
 * 4. 若 prices[i] - minPrice > maxP，说明以当前价卖出利润更大，更新 maxP
 * 5. 遍历结束返回 maxP
 *
 * 【执行示例】prices = [7, 1, 5, 3, 6, 4]
 *
 * maxP=0, minPrice=7
 * i=0: 7 < 7? 否 | 7-7=0 > 0? 否 | maxP=0, minPrice=7
 * i=1: 1 < 7? 是 → minPrice=1 | 1-1=0 > 0? 否
 * i=2: 5 < 1? 否 | 5-1=4 > 0? 是 → maxP=4
 * i=3: 3 < 1? 否 | 3-1=2 > 4? 否
 * i=4: 6 < 1? 否 | 6-1=5 > 4? 是 → maxP=5
 * i=5: 4 < 1? 否 | 4-1=3 > 5? 否
 * return 5 ✓（第2天买入1，第5天卖出6，利润5）
 *
 * 【时间复杂度】O(n) - 一次遍历数组
 * 【空间复杂度】O(1) - 只使用两个变量
 *
 * 【关键点】
 * - 第5行：maxP 初始化为 0，处理价格持续下跌的情况（不交易利润为0）
 * - 第6行：minPrice 初始化为价格数组的第0个元素
 * - 第8-9行：先判断是否更新最低价（"买入"逻辑必须在"卖出"之前）
 * - 第10-11行：再判断以当前价格卖出能获得多少利润
 *
 * 【注意】
 * - 第6行：minPrice 不能初始化为 Integer.MAX_VALUE，否则 if 条件永远不成立
 *   实际上初始化为 prices[0] 即可，因为必须买入某一天
 * - 第8行：判断用 < 而非 <=（等于时更新不改变结果，但也不影响正确性）
 * - 必须先买入才能卖出，遍历顺序天然保证了这一点（i 递增）
 * - 本题是"最多一笔交易"，不同于 LC 122（多笔交易）和 LC 123（最多两笔）
 */
public class LC_121_maxProfit_1 {
    public static int maxProfit(int[] prices) {
        int maxP=0;
        int minPrice=prices[0];
        for (int i = 0; i < prices.length; i++) {
            if(prices[i]<minPrice)
                minPrice=prices[i];
            if(prices[i]-minPrice>maxP)
                maxP=prices[i]-minPrice;
        }
        return maxP;
    }

    public static void main(String[] args) {
        System.out.println(maxProfit(new int[]{7, 1, 5, 3, 6, 4}));
    }
}
