package 贪心;

/**
 * LeetCode 122. 买卖股票的最佳时机 II - 解法一：贪心（累加所有上涨差价）
 *
 * 【题目描述】
 * 给你一个整数数组 prices，其中 prices[i] 表示某支股票第 i 天的价格。
 * 在每一天，你可以决定是否购买和/或出售股票。你在任何时候最多只能持有一股股票。
 * 你也可以先购买，然后在同一天出售。返回你能获得的最大利润。
 *
 * 【示例】
 * 输入：prices = [7,1,5,3,6,4]
 * 输出：7
 * 解释：第2天买入(1)，第3天卖出(5)，利润4；第4天买入(3)，第5天卖出(6)，利润3；总利润7
 * 输入：prices = [1,2,3,4,5]
 * 输出：4
 * 解释：第1天买入(1)，第5天卖出(5)，利润4（等同于每天买卖：1→2→3→4→5 累加差价）
 *
 * 【解题思路】
 * 核心思想：贪心——只要明天的价格比今天高，就在今天买入、明天卖出
 *
 * 由于可以无限次交易且不限制交易频率，可以将所有"上涨"的差价收入囊中：
 *   - 遍历相邻两天的价格差
 *   - 若 prices[i+1] > prices[i]，则累加差价 prices[i+1] - prices[i]
 *   - 等价于：抓住每一个上涨波段，忽略所有下跌
 *
 * 正确性证明：
 *   若 prices[a] < prices[b] < prices[c]，
 *   则 (prices[b]-prices[a]) + (prices[c]-prices[b]) = prices[c]-prices[a]
 *   即"逐日累加"与"持有跨越"的利润相同，贪心不会错过任何利润。
 *
 * 算法流程：
 * 1. 初始化 sum=0
 * 2. 遍历 i 从 0 到 n-2：
 *    若 prices[i+1] > prices[i]，sum += prices[i+1] - prices[i]
 * 3. 返回 sum
 *
 * 【执行示例】prices = [7,1,5,3,6,4]
 *
 * sum=0
 * i=0: 1>7? 否, sum=0
 * i=1: 5>1? 是, sum+=4=4
 * i=2: 3>5? 否, sum=4
 * i=3: 6>3? 是, sum+=3=7
 * i=4: 4>6? 否, sum=7
 * return 7 ✓
 *
 * 【时间复杂度】O(n) - 一次遍历
 * 【空间复杂度】O(1) - 只使用一个变量
 *
 * 【关键点】
 * - 第5行：sum 累加所有正的价格差
 * - 第6行：循环到 n-2，因为比较 prices[i+1] 和 prices[i]
 * - 第7-8行：只累加涨价部分，跌价忽略不计
 *
 * 【注意】
 * - 第6行：循环上界是 prices.length-1（即 n-2），不是 n-1
 * - 本题与 LC 121（只能交易一次）的区别：可以无限次交易
 * - 贪心策略在此题中是最优解，因为不限交易次数
 */
public class LC_122_maxProfit_1 {
    public int maxProfit(int[] prices) {
        int sum=0;
        for (int i = 0; i < prices.length-1; i++) {
            if(prices[i+1]>prices[i])
                sum+=prices[i+1]-prices[i];
        }
        return sum;
    }
}
