package 堆;

import java.util.PriorityQueue;

/**
 * LeetCode 215. 数组中的第K个最大元素 - 解法一：小顶堆
 *
 * 【题目描述】
 * 给定整数数组 nums 和整数 k，请返回数组中第 k 个最大的元素。
 * 请注意，你需要找的是数组排序后的第 k 个最大的元素，而不是第 k 个不同的元素。
 * 你必须设计并实现时间复杂度为 O(n) 的算法解决此问题。
 *
 * 【示例】
 * 输入：[3,2,1,5,6,4], k = 2
 * 输出：5
 * 输入：[3,2,3,1,2,4,5,5,6], k = 4
 * 输出：4
 *
 * 【解题思路】
 * 核心思想：使用大小为 k 的小顶堆维护 Top-K 中的最小值
 *
 * 遍历数组，小顶堆中始终保存当前最大的 k 个元素：
 *   - 堆未满（size < k）：直接入堆
 *   - 堆已满（size == k）：若当前元素 > 堆顶（最小的 Top-K 元素），
 *     则弹出堆顶并加入当前元素
 *   - 遍历结束后，堆顶就是第 k 大的元素
 *
 * 算法流程：
 * 1. 创建小顶堆 PriorityQueue（默认即小顶堆）
 * 2. 遍历数组每个元素：
 *    a. 若堆中元素不足 k 个，直接入堆
 *    b. 若堆已满且当前元素 > 堆顶，弹出堆顶，加入当前元素
 * 3. 返回堆顶元素
 *
 * 【执行示例】nums = [3,2,1,5,6,4], k = 2
 *
 * 堆初始为空
 * 3: 堆未满 → 入堆 → [3]
 * 2: 堆未满 → 入堆 → [2, 3]（堆顶2是两者中较小的）
 * 1: 堆已满, 1 < 堆顶(2) → 不处理 → [2, 3]
 * 5: 堆已满, 5 > 堆顶(2) → poll(2), add(5) → [3, 5]
 * 6: 堆已满, 6 > 堆顶(3) → poll(3), add(6) → [5, 6]
 * 4: 堆已满, 4 < 堆顶(5) → 不处理 → [5, 6]
 * return 堆顶 5 ✓
 *
 * 【时间复杂度】O(n log k) - 每次堆操作 O(log k)，总共 n 次操作
 * 【空间复杂度】O(k) - 堆中最多 k 个元素
 *
 * 【关键点】
 * - 第7行：PriorityQueue 默认是小顶堆（最小元素在堆顶）
 * - 第13-14行：堆未满时直接添加元素，用于初始的 k 个填充
 * - 第16行：堆已满时，只有大于堆顶的元素才可能替换堆内元素
 * - 第17-18行：弹出堆顶（当前第 k 大），加入更大的元素
 * - 第20行：最终堆顶即为第 k 大的元素
 *
 * 【注意】
 * - 第9行：堆未满的判断 size()<k，不能漏掉
 * - 第16行：条件 queue.peek()<num 用 < 而非 <=，等于时保留原元素不影响结果
 * - 此法适合数据量大、k 相对较小的场景（堆操作 O(log k) 比全排序 O(n log n) 好）
 * - 另一种 O(n) 解法是用快速选择（QuickSelect），但最坏情况 O(n²)
 */
public class LC_215_findKthLargest_1 {
    public int findKthLargest(int[] nums, int k) {
        PriorityQueue<Integer> queue=new PriorityQueue<>();
        for (int num : nums) {
            if(queue.size()<k){
                queue.add(num);
            }else{
                if(queue.peek()<num){
                    queue.poll();
                    queue.add(num);
                }
            }
        }
        return queue.peek();
    }
}
