package 普通数组;

import java.util.Arrays;

/**
 * LeetCode 169. 多数元素 - 解法一：排序
 *
 * 【题目描述】
 * 给定一个大小为 n 的数组 nums，返回其中的多数元素。多数元素是指在数组中出现次数大于 ⌊n/2⌋ 的元素。
 * 你可以假设数组是非空的，并且给定的数组总是存在多数元素。
 *
 * 【示例】
 * 输入：nums = [3,2,3]
 * 输出：3
 * 输入：nums = [2,2,1,1,1,2,2]
 * 输出：2
 *
 * 【解题思路】
 * 核心思想：排序后，下标 n/2 处的元素一定是多数元素
 *
 * 多数元素出现次数 > n/2，排序后无论从哪个位置开始，
 * 下标 n/2 处必然落在这个元素的覆盖范围内。
 *
 * 算法流程：
 * 1. 对数组排序
 * 2. 返回 nums[nums.length/2]
 *
 * 【执行示例】nums = [2,2,1,1,1,2,2]
 *
 * 排序后：[1,1,1,2,2,2,2]
 * 长度=7, nums[7/2]=nums[3]=2 ✓
 *
 * 【时间复杂度】O(n log n) - Arrays.sort 采用双轴快排
 * 【空间复杂度】O(1) - 原地排序（或 O(n) 取决于排序实现）
 *
 * 【关键点】
 * - 第8行：排序后直接取中位数，利用多数元素定义保证正确性
 * - 前提：题目保证存在多数元素，否则此方法无效
 *
 * 【注意】
 * - 此解法简单但非最优，O(n) 解法见 LC_169_majorityElement_2
 */
public class LC_169_majorityElement_1 {
    public int majorityElement(int[] nums) {
        Arrays.sort(nums);
        return nums[nums.length/2];
    }
}
