package 普通数组;

/**
 * LeetCode 41. 缺失的第一个正数
 * 
 * 【题目描述】
 * 给你一个未排序的整数数组 nums ，请你找出其中没有出现的最小的正整数。
 * 要求实现时间复杂度为 O(n) 并且只使用常数级别额外空间的解决方案。
 * 
 * 【示例】
 * 输入：nums = [1,2,0]
 * 输出：3
 * 解释：范围 [1,2] 中的数字都在数组中，3 是第一个缺失的正整数
 * 
 * 输入：nums = [3,4,-1,1]
 * 输出：2
 * 解释：1 在数组中，但 2 不在
 * 
 * 【解题思路】
 * 核心思想：利用辅助数组标记出现的正整数
 * 
 * 关键观察：
 * - 如果数组长度为 n，答案一定在 [1, n+1] 范围内
 * - 原因：最理想情况是数组包含 [1,2,...,n]，此时答案为 n+1
 * 
 * 算法流程：
 * 1. 创建长度为 n 的辅助数组 arr，初始化为 0
 * 2. 遍历 nums：
 *    - 如果 nums[i] 在 [1, n] 范围内，标记 arr[nums[i]-1] = nums[i]
 *    - 即：数字 1 放在索引 0，数字 2 放在索引 1，以此类推
 * 3. 遍历 arr：
 *    - 找到第一个值为 0 的位置 i，返回 i+1
 *    - 如果所有位置都不为 0，返回 n+1
 * 
 * 【执行示例】nums = [3,4,-1,1]
 * n = 4, arr = [0,0,0,0]
 * 
 * 遍历 nums：
 * i=0: nums[0]=3 ∈ [1,4] → arr[2]=3 → arr=[0,0,3,0]
 * i=1: nums[1]=4 ∈ [1,4] → arr[3]=4 → arr=[0,0,3,4]
 * i=2: nums[2]=-1 ∉ [1,4] → 跳过
 * i=3: nums[3]=1 ∈ [1,4] → arr[0]=1 → arr=[1,0,3,4]
 * 
 * 遍历 arr：
 * i=0: arr[0]=1 ≠ 0
 * i=1: arr[1]=0 → 返回 i+1=2 ✓
 * 
 * 【时间复杂度】O(n) - 两次遍历
 * 【空间复杂度】O(n) - 辅助数组
 * 
 * 【注意】
 * - 题目要求 O(1) 空间，你的解法使用了 O(n) 空间
 * - 最优解法是原地哈希：将 nums[i] 放到索引 nums[i]-1 的位置
 * - 通过交换实现原地重排，无需额外数组
 * 
 * 【关键点】
 * - 第7-9行：标记出现的正整数，只处理 [1, n] 范围内的数
 * - 第10-13行：找到第一个未被标记的位置
 * - 第14行：如果所有位置都被标记，返回 n+1
 * 
 * 【边界情况】
 * - nums = [1,2,3] → arr=[1,2,3] → 返回 4
 * - nums = [7,8,9] → arr=[0,0,0] → 返回 1
 * - nums = [] → 返回 1
 */
public class LC_41_firstMissingPositive_1 {
    public static int firstMissingPositive(int[] nums) {
        // 辅助数组，用于标记出现的正整数
        int[] arr=new int[nums.length];
        for (int i = 0; i < nums.length; i++) {
            // 只处理 [1, n] 范围内的正整数
            if(nums[i]>0&&nums[i]<nums.length+1)
                arr[nums[i]-1]=nums[i];  // 数字 x 放在索引 x-1
        }
        // 找到第一个未被标记的位置
        for (int i = 0; i < arr.length; i++) {
            if(arr[i]==0)
                return i+1;  // 索引 i 对应数字 i+1
        }
        // 所有位置都被标记，返回 n+1
        return nums.length+1;
    }

}
