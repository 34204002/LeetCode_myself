package 普通数组;

/**
 * LeetCode 53. 最大子数组和
 * 
 * 【题目描述】
 * 给你一个整数数组 nums ，请你找出一个具有最大和的连续子数组（子数组最少包含一个元素），返回其最大和。
 * 子数组是数组中的一个连续部分。
 * 
 * 【示例】
 * 输入：nums = [-2,1,-3,4,-1,2,1,-5,4]
 * 输出：6
 * 解释：连续子数组 [4,-1,2,1] 的和最大，为 6。
 * 
 * 【解题思路】
 * 核心思想：前缀和 + 维护最小前缀和
 * 
 * 关键观察：
 * - 子数组 [i+1, j] 的和 = sums[j] - sums[i]
 * - 要使差值最大，需要 sums[j] 尽可能大，sums[i] 尽可能小
 * - 遍历过程中维护最小的前缀和 min
 * 
 * 算法流程：
 * 1. 计算前缀和数组 sums：
 *    - sums[0] = 0（空前缀）
 *    - sums[i] = nums[0] + nums[1] + ... + nums[i-1]
 * 
 * 2. 遍历前缀和数组：
 *    - 计算当前前缀和与最小前缀和的差值：sums[i] - min
 *    - 更新最大差值 max
 *    - 更新最小前缀和 min
 * 
 * 【执行示例】nums = [-2,1,-3,4,-1,2,1,-5,4]
 * 
 * 前缀和：
 * sums[0] = 0
 * sums[1] = 0 + (-2) = -2
 * sums[2] = -2 + 1 = -1
 * sums[3] = -1 + (-3) = -4
 * sums[4] = -4 + 4 = 0
 * sums[5] = 0 + (-1) = -1
 * sums[6] = -1 + 2 = 1
 * sums[7] = 1 + 1 = 2
 * sums[8] = 2 + (-5) = -3
 * sums[9] = -3 + 4 = 1
 * sums = [0, -2, -1, -4, 0, -1, 1, 2, -3, 1]
 * 
 * 遍历过程：
 * i=1: max = max(-2-0, MIN) = -2, min = min(-2, 0) = -2
 * i=2: max = max(-1-(-2), -2) = 1, min = min(-1, -2) = -2
 * i=3: max = max(-4-(-2), 1) = 1, min = min(-4, -2) = -4
 * i=4: max = max(0-(-4), 1) = 4, min = min(0, -4) = -4
 * i=5: max = max(-1-(-4), 4) = 4, min = min(-1, -4) = -4
 * i=6: max = max(1-(-4), 4) = 5, min = min(1, -4) = -4
 * i=7: max = max(2-(-4), 5) = 6, min = min(2, -4) = -4
 * i=8: max = max(-3-(-4), 6) = 6, min = min(-3, -4) = -4
 * i=9: max = max(1-(-4), 6) = 6, min = min(1, -4) = -4
 * 
 * 结果：max = 6 ✓（对应子数组 [4,-1,2,1]）
 * 
 * 【时间复杂度】O(n) - 两次遍历
 * 【空间复杂度】O(n) - 前缀和数组
 * 
 * 【优化建议】
 * - Kadane 算法：只需一次遍历，O(1) 空间
 * - 维护当前子数组和 curSum，如果 curSum < 0 则重置为 0
 * - 每次更新全局最大值
 * 
 * 【关键点】
 * - 第6-10行：计算前缀和数组，sums[0]=0 作为基准
 * - 第11-12行：初始化 min=0（空前缀的和），max=MIN_VALUE
 * - 第13-17行：遍历前缀和，维护最小前缀和并计算最大差值
 * 
 * 【为什么 min 初始化为 0？】
 * - sums[0]=0 表示空前缀，允许子数组从索引 0 开始
 * - 如果初始化为 sums[1]，会漏掉以 nums[0] 开头的子数组
 */
public class LC_53_maxSubArray_1 {
    public static int maxSubArray(int[] nums) {

        // 计算前缀和数组
        int[] sums=new int[nums.length+1];
        sums[0]=0;  // 空前缀的和为 0
        for (int i = 1; i < sums.length; i++) {
            sums[i]=sums[i-1]+nums[i-1];
        }
        int min=0;  // 最小前缀和，初始为 0（空前缀）
        int max=Integer.MIN_VALUE;  // 最大子数组和
        // 遍历前缀和，计算最大差值
        for (int i = 1; i < sums.length; i++) {
            max=Math.max(sums[i]-min,max);  // 更新最大子数组和
            min=Math.min(sums[i],min);      // 更新最小前缀和

        }
        return max;
    }
}
