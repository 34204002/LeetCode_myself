package 普通数组;

import java.util.*;

/**
 * LeetCode 189. 轮转数组
 * 
 * 【题目描述】
 * 给定一个整数数组 nums，将数组中的元素向右轮转 k 个位置，其中 k 是非负数。
 * 
 * 【示例】
 * 输入: nums = [1,2,3,4,5,6,7], k = 3
 * 输出: [5,6,7,1,2,3,4]
 * 解释:
 * 向右轮转 1 步: [7,1,2,3,4,5,6]
 * 向右轮转 2 步: [6,7,1,2,3,4,5]
 * 向右轮转 3 步: [5,6,7,1,2,3,4]
 * 
 * 【解题思路】
 * 核心思想：使用双端队列模拟轮转操作
 * 
 * 1. 将数组所有元素加入双端队列（尾部添加）
 * 2. 执行 k 次轮转：
 *    - 从队尾弹出一个元素（pollLast）
 *    - 将该元素添加到队首（addFirst）
 *    - 相当于整体向右移动一位
 * 3. 将队列中的元素写回原数组
 * 
 * 【执行示例】nums = [1,2,3,4,5,6,7], k = 3
 * 初始 deque: [1,2,3,4,5,6,7]
 * 
 * 第1次轮转: pollLast=7, addFirst → [7,1,2,3,4,5,6]
 * 第2次轮转: pollLast=6, addFirst → [6,7,1,2,3,4,5]
 * 第3次轮转: pollLast=5, addFirst → [5,6,7,1,2,3,4]
 * 
 * 写回数组: nums = [5,6,7,1,2,3,4] ✓
 * 
 * 【时间复杂度】O(n + k)
 * - 构建队列 O(n)
 * - k 次轮转操作 O(k)
 * - 写回数组 O(n)
 * 
 * 【空间复杂度】O(n) - 双端队列存储所有元素
 * 
 * 【优化建议】
 * - 方法二：三次反转法（原地操作，O(1) 空间）
 *   · 反转整个数组
 *   · 反转前 k 个元素
 *   · 反转后 n-k 个元素
 * - 方法三：环形替换（原地操作，O(1) 空间）
 * 
 * 【关键点】
 * - 第7-10行：将数组元素加入双端队列
 * - 第11-14行：执行 k 次轮转，每次从尾部移到头部
 * - 第15-19行：将队列元素写回原数组
 * 
 * 【注意】
 * - 当 k > n 时，实际轮转次数应为 k % n
 * - 你的代码未处理此情况，可能导致不必要的重复操作
 * - 建议在循环前添加：k = k % nums.length;
 */
public class LC_189_rotate_1 {
    public static void rotate(int[] nums, int k) {
        Deque<Integer> deque=new ArrayDeque<>();
        // 将数组元素加入双端队列
        for (int num : nums) {
            deque.addLast(num);
        }
        // 执行 k 次轮转：从尾部移到头部
        for (int i = 0; i < k; i++) {
            int n=deque.pollLast();
            deque.addFirst(n);
        }
        // 将队列元素写回原数组
        int count=0;
        for (Integer i : deque) {
            nums[count]=i;
            count++;
        }
    }
}
