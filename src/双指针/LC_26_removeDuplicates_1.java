package 双指针;

/**
 * LeetCode 26. 删除有序数组中的重复项 - 解法一：双指针
 *
 * 【题目描述】
 * 给你一个非严格递增排列的数组 nums，请你原地删除重复出现的元素，使每个元素只出现一次，
 * 返回删除后数组的新长度。元素的相对顺序应该保持一致。
 * 不要使用额外的数组空间，你必须在原地修改输入数组。
 *
 * 【示例】
 * 输入：nums = [0,0,1,1,1,2,2,3,3,4]
 * 输出：5, nums = [0,1,2,3,4,_,_,_,_,_]
 * 输入：nums = [1,1,2]
 * 输出：2, nums = [1,2,_]
 *
 * 【解题思路】
 * 核心思想：快慢双指针，快指针扫描非重复元素，慢指针标记已处理部分的末尾
 *
 * 算法流程：
 * 1. p1 指向已处理部分的最后一个元素（初始 0），p2 向后扫描
 * 2. 跳过所有与 nums[p1] 相等的元素
 * 3. 若 p2 越界则退出；否则 p1 前移，将 nums[p2] 复制到 nums[p1]
 * 4. 返回 p1+1（新长度）
 *
 * 【执行示例】nums = [0,0,1,1,1,2,2,3,3,4]
 *
 * p1=0, p2=1
 * p2=1: nums[0]==nums[1](0==0) → p2=2
 * p2=2: nums[0]!=nums[2](0!=1) → 退出内层循环, p2!=len
 * p1=1, nums[1]=nums[2]=1 → [0,1,1,1,1,2,2,3,3,4], p2=3
 *
 * p1=1, p2=3
 * p2=3: nums[1]==nums[3](1==1) → p2=4
 * p2=4: nums[1]==nums[4](1==1) → p2=5
 * p2=5: nums[1]!=nums[5](1!=2) → 退出
 * p1=2, nums[2]=nums[5]=2 → [0,1,2,1,1,2,2,3,3,4], p2=6
 *
 * ...依此类推
 * 最终 p1=4, return 5 ✓
 *
 * 【时间复杂度】O(n) - 每个元素最多被 p2 遍历一次
 * 【空间复杂度】O(1) - 原地修改
 *
 * 【关键点】
 * - 第6行：p1 指向已确认的最后一个唯一元素，p2 是扫描指针
 * - 第8行：跳过所有重复值，直到找到下一个不同的元素
 * - 第10-11行：若 p2 走到末尾说明后面全是重复，直接结束
 * - 第12-13行：找到新元素，p1 前移并复制
 * - 第15行：返回长度 = p1+1（p1 是下标，长度加 1）
 *
 * 【注意】
 * - 第8行：内层 while 条件中 p2 < nums.length 必须在前面防止越界
 * - 第12行：p1 先自增再赋值，因为 p1 指向的是最后一个已处理位置
 * - 输入数组已排序是使用此方法的前提
 */
public class LC_26_removeDuplicates_1 {
    public int removeDuplicates(int[] nums) {
        int p1 = 0;
        int p2 = 1;
        while (p1 < nums.length && p2 < nums.length) {
            while (p2<nums.length&&nums[p1]==nums[p2]){
                p2++;
            }
            if(p2==nums.length)
                break;
            p1++;
            nums[p1]=nums[p2];
        }
        return p1+1;
    }
}
