package 双指针;

/**
 * LeetCode 283. 移动零
 * 
 * 【题目描述】
 * 给定一个数组 nums，编写一个函数将所有 0 移动到数组的末尾，同时保持非零元素的相对顺序。
 * 必须在原数组上操作，不能拷贝额外的数组。
 * 
 * 【示例】
 * 输入: nums = [0,1,0,3,12]
 * 输出: [1,3,12,0,0]
 * 
 * 【解题思路】
 * 核心思想：双指针交换法
 * 
 * p1：指向下一个应该放置非零元素的位置（也指向当前的0）
 * p2：向前扫描寻找非零元素
 * 
 * 工作流程：
 * 1. 如果 nums[p1] != 0：两个指针都前移（当前位置已正确）
 * 2. 如果 nums[p1] == 0：
 *    - p2 向前寻找非零元素
 *    - 找到后交换 nums[p1] 和 nums[p2]
 *    - p1 前移（该位置已填入非零元素）
 * 
 * 【执行示例】nums = [0,1,0,3,12]
 * 初始：p1=0, p2=0 → nums[0]=0，p2找非零
 * p2=1: nums[1]=1≠0 → 交换 → [1,0,0,3,12], p1=1
 * p1=1: nums[1]=0，p2找非零
 * p2=2: nums[2]=0，继续
 * p2=3: nums[3]=3≠0 → 交换 → [1,3,0,0,12], p1=2
 * ...最终得到 [1,3,12,0,0]
 * 
 * 【时间复杂度】O(n) - 每个元素最多被访问两次
 * 【空间复杂度】O(1) - 原地操作
 * 
 * 【对比其他方法】
 * - 方法二：先填充非零元素，再补零（更简洁）
 * - 你的方法：双指针交换，保持了相对顺序
 */
public class LC_283_moveZeroes_1 {
    public static void moveZeroes(int[] nums) {
        int p1=0;//p1指向0的位置（待填充位置）
        int p2=0;//p2向前扫描寻找非0元素
        while (p1<nums.length-1&&p2<nums.length-1){
            if(nums[p1]!=0){
                // 当前位置非0，两个指针都前移
                p1++;
                p2++;
            }else {
                // p1指向0，p2寻找非0元素
                p2++;
                if(nums[p2]!=0){
                    // 找到非0元素，交换到p1位置
                    int n=nums[p1];
                    nums[p1]=nums[p2];
                    nums[p2]=n;
                    p1++;  // p1位置已填好，前移
                }
            }
        }
    }
}
