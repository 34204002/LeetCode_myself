package 动态规划;

/**
 * LeetCode 198. 打家劫舍 - 解法一：动态规划
 *
 * 【题目描述】
 * 你是一个专业的小偷，计划偷窃沿街的房屋。每间房内都藏有一定的现金，
 * 影响你偷窃的唯一制约因素就是相邻的房屋装有相互连通的防盗系统，
 * 如果两间相邻的房屋在同一晚上被小偷闯入，系统会自动报警。
 * 给定一个代表每个房屋存放金额的非负整数数组，计算你不触动警报装置的情况下，
 * 一夜之内能够偷窃到的最高金额。
 *
 * 【示例】
 * 输入：nums = [1,2,3,1]
 * 输出：4
 * 解释：偷 1 号房屋(金额=1) + 3 号房屋(金额=3) = 4
 * 输入：nums = [2,7,9,3,1]
 * 输出：12
 * 解释：偷 1 号(2) + 3 号(9) + 5 号(1) = 12
 *
 * 【解题思路】
 * 核心思想：动态规划，dp[i] 表示偷前 i 间房屋能获得的最高金额
 *
 * 状态定义：
 * - dp[i]：偷窃前 i 间房屋（含第 i 间）能获得的最高金额
 *
 * 状态转移方程：
 * - dp[i] = max(dp[i-1], dp[i-2] + nums[i])
 * - 解释：到达第 i 间房屋时有两种选择：
 *   1. 不偷当前房屋：最高金额 = dp[i-1]（前 i-1 间的最大收益）
 *   2. 偷当前房屋：dp[i-2] + nums[i]（前 i-2 间的最大收益 + 当前金额）
 *
 * 边界条件：
 * - dp[0] = nums[0]（只有一间房屋，直接偷）
 * - dp[1] = max(nums[0], nums[1])（两间房屋，偷金额大的那间）
 *
 * 算法流程：
 * 1. 处理边界：n=1 直接返回 nums[0]，n=2 返回 max(nums[0], nums[1])
 * 2. 初始化 dp[0]=nums[0], dp[1]=max(nums[0], nums[1])
 * 3. 从 i=2 到 n-1 递推 dp[i]
 * 4. 返回 dp[n-1]
 *
 * 【执行示例】nums = [1,2,3,1]
 *
 * n=4, 跳过 n=1 和 n=2 的判断
 * dp[0]=1, dp[1]=max(1,2)=2
 * i=2: dp[2]=max(dp[1], dp[0]+nums[2]) = max(2, 1+3=4) = 4
 * i=3: dp[3]=max(dp[2], dp[1]+nums[3]) = max(4, 2+1=3) = 4
 * return dp[3]=4 ✓（偷第1间和第3间：1+3=4）
 *
 * 【执行示例】nums = [2,7,9,3,1]
 *
 * dp[0]=2, dp[1]=max(2,7)=7
 * i=2: dp[2]=max(7, 2+9=11) = 11
 * i=3: dp[3]=max(11, 7+3=10) = 11
 * i=4: dp[4]=max(11, 11+1=12) = 12 ✓（偷第1、3、5间：2+9+1=12）
 *
 * 【时间复杂度】O(n) - 一次遍历
 * 【空间复杂度】O(n) - dp 数组大小 n，可优化为 O(1)（只保留前两个状态）
 *
 * 【关键点】
 * - 第7-8行：处理 n=1 和 n=2 的边界情况
 * - 第12行：dp[i] = max(dp[i-1], dp[i-2]+nums[i]) 为核心转移方程
 * - 第14行：最终答案在 dp[n-1]
 *
 * 【注意】
 * - 第7行：长度判断要写在 dp 数组创建之前，否则若 n=1 时 dp[1] 会越界
 * - 第9-10行：n=2 时直接返回两数的较大值
 * - 第12行：dp[i-1] 对应"不偷当前"的最优解，dp[i-2]+nums[i] 对应"偷当前"的最优解
 * - 本题隐含的限制条件是不能偷相邻房屋，状态转移已包含该约束
 */
public class LC_198_rob_1 {
    public int rob(int[] nums) {
        int[] dp=new int[nums.length];
        if(nums.length==1){
            return nums[0];
        }
        if(nums.length==2){
            return Math.max(nums[0],nums[1]);
        }
        dp[0]=nums[0];
        dp[1]=Math.max(nums[0],nums[1]);
        for (int i = 2; i < nums.length; i++) {
             dp[i]=Math.max(dp[i-1],dp[i-2]+nums[i]);
        }
        return dp[nums.length-1];
    }
}
