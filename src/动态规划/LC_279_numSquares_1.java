package 动态规划;

/**
 * LeetCode 279. 完全平方数 - 解法一：动态规划
 *
 * 【题目描述】
 * 给你一个整数 n，返回和为 n 的完全平方数的最少数量。
 * 完全平方数是一个整数，其值等于另一个整数的平方；换句话说，其值等于一个整数自乘的积。
 * 例如 1、4、9 和 16 都是完全平方数，而 3 和 11 不是。
 *
 * 【示例】
 * 输入：n = 12
 * 输出：3
 * 解释：12 = 4 + 4 + 4（最少 3 个完全平方数）
 * 输入：n = 13
 * 输出：2
 * 解释：13 = 4 + 9（最少 2 个完全平方数）
 *
 * 【解题思路】
 * 核心思想：一维 DP，dp[i] 表示组成 i 的最少完全平方数个数
 *
 * 状态定义：
 * - dp[i]：和为 i 的完全平方数的最少数量
 *
 * 状态转移：
 * - 对于每个 i，尝试所有 j 满足 j*j <= i
 * - dp[i] = min(dp[i], dp[i - j*j] + 1)
 * - 即：在当前 i 中减去一个完全平方数 j*j 后的最优解 + 1
 *
 * 边界条件：
 * - dp[0] = 0（已由 int 默认值 0 表示）
 *
 * 算法流程：
 * 1. 创建长度 n+1 的 dp 数组，全部默认 0
 * 2. 外层循环 i 从 0 到 n：
 *    a. 内层循环 j 从 1 到 j*j <= i：
 *       - 首次遇到时 dp[i] 初始化为 MAX_VALUE，方便取 min
 *       - dp[i] = min(dp[i], dp[i-j*j] + 1)
 * 3. 返回 dp[n]
 *
 * 【执行示例】n = 12
 *
 * dp[0]=0
 * i=1: j=1(1*1≤1) dp[1]=min(MAX, dp[0]+1=1)=1
 * i=2: j=1(1*1≤2) dp[2]=min(MAX, dp[1]+1=2)=2
 * i=3: j=1(1*1≤3) dp[3]=min(MAX, dp[2]+1=3)=3
 * i=4: j=1→dp[4]=min(MAX, dp[3]+1=4)=4; j=2(2*2≤4) dp[4]=min(4, dp[0]+1=1)=1
 * i=5: j=1→dp[5]=min(MAX, dp[4]+1=2)=2; j=2→dp[5]=min(2, dp[1]+1=2)=2
 * ...
 * i=12: j=1→dp[12]=min(MAX, dp[11]+1=...)=...; j=2→min(..., dp[8]+1=3)=3; j=3→min(3, dp[3]+1=4)=3
 * return dp[12]=3 ✓（12 = 4+4+4，3个完全平方数）
 *
 * 【时间复杂度】O(n * √n) - 外层 n 次，内层 √n 次
 * 【空间复杂度】O(n) - dp 数组长度 n+1
 *
 * 【关键点】
 * - 第9行：dp[i] 默认 0，首次进入内层循环需设为 MAX_VALUE
 * - 第11行：dp[i-j*j] + 1 表示「先减去一个完全平方数 j*j，然后 +1」
 * - dp[0] 必须为 0，这是基本边界
 *
 * 【注意】
 * - 第9行：dp[i]==0 的判断隐含了 dp[0]=0 不会被修改（因为 0 不需要完全平方数）
 * - 第10行：Integer.MAX_VALUE 用于后续 Math.min 比较，不能直接用 0 初始化
 * - 第11行：dp[i-j*j] 取之前计算好的子问题最优解
 * - 完全平方数最多到 √n，内层循环 j 从 1 开始
 */
public class LC_279_numSquares_1 {
    public int numSquares(int n) {
        int[] dp=new int[n+1];

        for (int i = 0; i <= n; i++) {
            for (int j = 1; j*j <= i; j++) {
                if(dp[i]==0)
                    dp[i]=Integer.MAX_VALUE;
                dp[i]=Math.min(dp[i-j*j]+1,dp[i]);
            }
        }
        return dp[n];
    }
}
