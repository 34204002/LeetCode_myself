package 动态规划;

import java.util.ArrayList;

/**
 * LeetCode 70. 爬楼梯
 * 
 * 【题目描述】
 * 假设你正在爬楼梯。需要 n 阶你才能到达楼顶。
 * 每次你可以爬 1 或 2 个台阶。你有多少种不同的方法可以爬到楼顶呢？
 * 
 * 【示例】
 * 输入：n = 3
 * 输出：3
 * 解释：有三种方法可以爬到楼顶。
 * 1. 1 阶 + 1 阶 + 1 阶
 * 2. 1 阶 + 2 阶
 * 3. 2 阶 + 1 阶
 * 
 * 【解题思路】
 * 核心思想：动态规划（斐波那契数列）
 * 
 * 状态定义：
 * - arr[i]：到达第 i 阶楼梯的方法数
 * 
 * 状态转移方程：
 * - arr[i] = arr[i-1] + arr[i-2]
 * - 解释：到达第 i 阶有两种方式：
 *   1. 从第 i-1 阶爬 1 步上来（有 arr[i-1] 种方法）
 *   2. 从第 i-2 阶爬 2 步上来（有 arr[i-2] 种方法）
 * 
 * 边界条件：
 * - arr[0] = 1：地面到地面，1种方法（不动）
 * - arr[1] = 1：从地面到第1阶，1种方法（爬1步）
 * 
 * 算法流程：
 * 1. 创建数组 arr，大小为 45（题目限制 n ≤ 45）
 * 2. 初始化边界条件：arr[0] = 1, arr[1] = 1
 * 3. 从 i=2 开始递推计算到 i=44
 * 4. 返回 arr[n]
 * 
 * 【执行示例】n = 5
 * 
 * 初始：arr[0]=1, arr[1]=1
 * 
 * i=2: arr[2] = arr[1] + arr[0] = 1 + 1 = 2
 *      （方法：1+1 或 2）
 * 
 * i=3: arr[3] = arr[2] + arr[1] = 2 + 1 = 3
 *      （方法：1+1+1, 1+2, 2+1）
 * 
 * i=4: arr[4] = arr[3] + arr[2] = 3 + 2 = 5
 *      （方法：1+1+1+1, 1+1+2, 1+2+1, 2+1+1, 2+2）
 * 
 * i=5: arr[5] = arr[4] + arr[3] = 5 + 3 = 8
 *      结果：8 ✓
 * 
 * 【时间复杂度】O(n) - 单次循环计算
 * 【空间复杂度】O(n) - 数组存储所有状态
 * 
 * 【关键点】
 * - 第7-8行：初始化基础情况，到达第0阶和第1阶都只有1种方法
 * - 第10-12行：递推公式，当前阶数的方法数等于前两阶方法数之和
 * - 第13行：返回第 n 阶的方法数
 * 
 * 【注意事项】
 * - 第7行：数组大小固定为45，因为题目保证 1 <= n <= 45
 * - 实际上可以优化空间复杂度到 O(1)，只保存前两个状态即可
 * 
 * 【优化思路】
 * - 空间优化：只用两个变量 prev1 和 prev2，不需要整个数组
 * - 数学方法：直接使用斐波那契数列的通项公式
 */
public class LC_70_climbStairs_1 {
    public int climbStairs(int n) {
        int[] arr=new int[45];
        arr[0]=1;
        arr[1]=1;
        for (int i = 2; i < arr.length; i++) {
            arr[i]=arr[i-1]+arr[i-2];
        }
        return arr[n];
    }
}
